                    <p>Žuffová, M. What the Public Wants to Know? A Text-as-data Study of Information Requests to the UK Government.</p>

                    <p><strong>Abstract:</strong> <em>In this study, I contest the commonly-shared perception of access to information serving predominantly a government oversight function utilised by investigative journalists to reveal political corruption or misconduct. I argue that ordinary citizens seek information far beyond such a narrow definition of accountability. By applying text-as-data methods to over 36,000 information requests submitted to the UK central government through the platform Whatdotheyknow.com from 2008 to 2017, I show that requests serve as a valuable indicator of public responsiveness to policies. I demonstrate that citizens use them to bring forward their experiences with new policies, seek more clarification, and voice concerns about policies’ impacts on their lives. The results illustrate the great utility of information requests for the study of politics.</em></p>

